package bModel.bk;

public class BookList {
	int BBNUM;
	String BISBN;
	String BTITLE;
	String BWRITER;
	String BPUBLISHER;
	String BAGE;
	String BCAUSE;
	
	public String getBCAUSE() {
		return BCAUSE;
	}
	public void setBCAUSE(String bCAUSE) {
		BCAUSE = bCAUSE;
	}
	public int getBBNUM() {
		return BBNUM;
	}
	public void setBBNUM(int bBNUM) {
		BBNUM = bBNUM;
	}
	public String getBISBN() {
		return BISBN;
	}
	public void setBISBN(String bISBN) {
		BISBN = bISBN;
	}
	public String getBTITLE() {
		return BTITLE;
	}
	public void setBTITLE(String bTITLE) {
		BTITLE = bTITLE;
	}
	public String getBWRITER() {
		return BWRITER;
	}
	public void setBWRITER(String bWRITER) {
		BWRITER = bWRITER;
	}
	public String getBPUBLISHER() {
		return BPUBLISHER;
	}
	public void setBPUBLISHER(String bPUBLISHER) {
		BPUBLISHER = bPUBLISHER;
	}
	public String getBAGE() {
		return BAGE;
	}
	public void setBAGE(String bAGE) {
		BAGE = bAGE;
	}
	

}
